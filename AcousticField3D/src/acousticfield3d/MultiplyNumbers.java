    private void processButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processButtonActionPerformed
        try {
            final String sep = separatorText.getText();
            final double constant = Parse.stringToDouble( constantText.getText() );
            String[] content = new String(FileUtils.getBytesFromFile( new File(inputText.getText()))).split("\\n");
            StringBuilder sb = new StringBuilder();
            for(String l : content){
                String[] ns = l.split(sep);
                final int size = ns.length;
                int index = 0;
                for(String n : ns){
                    final double np = Parse.stringToDouble( n );
                    sb.append( (np*constant) + "");
                    if (index < size-1){
                        sb.append(sep);
                    }
                    index++;
                }
               
                sb.append("\n");
            }
            FileUtils.writeBytesInFile(new File(inputText.getText() + "_"), sb.toString().getBytes());
        } catch (IOException ex) {
            Logger.getLogger(MultiplyNumbers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_processButtonActionPerformed

