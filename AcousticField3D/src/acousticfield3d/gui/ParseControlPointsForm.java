    private void parseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_parseButtonActionPerformed
        String[] t = area.getText().replaceAll("\\r", "").split("\\n");
        final boolean isBead = beadsOrCPCheck.isSelected();
        
        int xPos = Integer.parseInt( xPosText.getText() );
        int yPos = Integer.parseInt( yPosText.getText() );
        int zPos = Integer.parseInt( zPosText.getText() );
        int nPos = Integer.parseInt( nPosText.getText() );
        int currentNumber = 0;
        int lastFrame = -1;
        
        for (String l : t){
            String[] parts = l.trim().split(" ");
            final int len = parts.length;
            try{
                float x = Float.parseFloat( parts[ xPos >= 0 ? xPos : len + xPos ] );
                float y = Float.parseFloat( parts[ yPos >= 0 ? yPos : len + yPos ] );
                float z = Float.parseFloat( parts[ zPos >= 0 ? zPos : len + zPos ] );
                int frame = Integer.parseInt( parts[ nPos >= 0 ? nPos : len + nPos ] );
                
                if (frame != lastFrame){
                    currentNumber = 0;
                    lastFrame = frame;
                } else {
                    currentNumber++;
                }
                
                mf.cpPanel.addControlPoint(x, y, z, frame, currentNumber, isBead);
            }catch(Exception e){}
        }
        mf.needUpdate();
    }//GEN-LAST:event_parseButtonActionPerformed

  

