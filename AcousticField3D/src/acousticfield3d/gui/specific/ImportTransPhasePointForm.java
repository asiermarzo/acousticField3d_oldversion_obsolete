    private void importButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importButtonActionPerformed
        final String[] lines = area.getText().split("\\n");
        for(String l : lines){
            final String[] ls = l.trim().split(" ");
            if (l.startsWith("t")){
                final Vector3f pos = new Vector3f(ls[1], ls[2], ls[3]);
                final Vector3f rot = new Vector3f(ls[4], ls[5], ls[6]);
                final float amp = Parse.stringToFloat( ls[7] );
                final float phase = Parse.stringToFloat( ls[8] );
                final Transducer t = mf.addTransducersForm.createTransducer();
                t.getTransform().setTranslation( pos );
                t.getTransform().getRotation().fromAngles(rot.x, rot.y, rot.z);
                t.setPhase( phase );
                t.setpAmplitude( amp );
                mf.addTransducersForm.addTransducer(t);
            }else if (l.startsWith("p")){
                final Vector3f pos = new Vector3f(ls[1], ls[2], ls[3]);
                mf.cpPanel.addControlPoint (pos.x, pos.y, pos.z, mf.cpPanel.getNumber(), 0, false);
            }
        }
        mf.initSimulation();
    }//GEN-LAST:event_importButtonActionPerformed


